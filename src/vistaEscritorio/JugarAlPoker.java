    @Override
    public void mostrarError(String message) {
        taMensajes.setText(message);
    }

    @Override
    public void mostrarTitulo(int numeroMesa, int numeroManoActual, String nombreCompleto, double saldoJugador) {
        String titulo = ("JUGADOR " + nombreCompleto.toUpperCase() + " - NUMERO DE MESA: " + numeroMesa + 
                " - NUMERO DE MANO ACTUAL: " + numeroManoActual + " - SALDO: " + saldoJugador);
        setTitle(titulo);
    }
    
    @Override
    public void mostrarJugadores(ArrayList<Jugador> listaJugadoresMesa){
        ArrayList<String> listadoJugadores = new ArrayList();
        for(Jugador j: listaJugadoresMesa){
            listadoJugadores.add("Nombre : " + j.getNombreCompleto() + " | Situaci√≥n : " + j.getSituacion());
        }
        listaJugadoresEnMesa.setListData(listadoJugadores.toArray());
    }

    @Override
    public void mostrarFiguras(ArrayList<Figura> listaFiguras) {
        ArrayList<String> listadoFigurasPosibles = new ArrayList();
        for(Figura f: listaFiguras){
            listadoFigurasPosibles.add("Nombre : " + f.getNombre());
        }
        listaFigurasPosibles.setListData(listaFiguras.toArray());
    }

    @Override
    public void mostrarEstadoMano(String descripcion) {
        labelEstadoMano.setText("Estado de la mano: " + descripcion);
    }

    @Override
    public void cargarCartas(ArrayList<CartaPoker> cartas) {
        try {
            panelCartasPoker.cargarCartas(cartas);
        } catch (PanelCartasPokerException ex) {
            taMensajes.setText(ex.getMessage());
        }
    }

