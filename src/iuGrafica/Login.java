    private void login() {
    String nombre = textoUsuario.getText();
    String password = new String(textoPassword.getPassword());

    try {
        Object resultado = llamarLogin(nombre, password);
        dispose();
        proximoCasoUso(resultado);
        
    } catch (CredencialesException e) {
        taErroresLogin.setText(e.getMessage());
        
    } catch (SesionException e) {
        taErroresLogin.setText(e.getMessage());;
    }
}

    
    public abstract Object llamarLogin(String nombre, String password) throws CredencialesException, SesionException;
    public abstract void proximoCasoUso(Object resultado);
    

